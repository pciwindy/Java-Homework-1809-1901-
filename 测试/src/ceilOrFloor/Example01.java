package ceilOrFloor;

public class Example01 {
	public static void main(String args[]){
        double d = 2.7;
        float f = 2.3f;    
 
        System.out.println(Math.ceil(d));
        System.out.println(Math.ceil(f)); 
                     
        System.out.println(Math.floor(d));
        System.out.println(Math.floor(f)); 
    }
}
